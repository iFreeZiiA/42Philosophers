#include "libft.h"

char	*ft_strchr(const char *str, int c)
{
	int	i;

	if (c > 255)
		c %= 256;
	i = 0;
	while (str[i] != '\0')
	{
		if (((unsigned char *)str)[i] == c)
			return ((char *)&str[i]);
		i++;
	}
	if (c == '\0')
		return ((char *)&str[i]);
	return (NULL);
}
